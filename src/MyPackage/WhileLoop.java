package MyPackage;

public class WhileLoop {

	public static void main(String[] args) {
	
		//While Loop
		
		int i=1;  //initialization
		
		while(i<=10){  //Condition
			System.out.println(i);
			System.out.println("My Name Is Raghavendra");
			i++;    // increment
		}	
		
		
		
		//Do While Loop
		int j=1;
		do{
			System.out.println("My Name Is Raghavendra Prabhu");
			j++;
			
		} while (j<=10);
		
		
		
		
		
	}
}
